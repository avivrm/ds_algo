package com.ds.gfg.linnkedlist;

public class Q2Node {

	int data;
	Q2Node next;
	
	Q2Node(){
		data = 0;
		next = null;
	}

	public int getData() {
		return data;
	}

	public void setData(int data) {
		this.data = data;
	}

	public Q2Node getNext() {
		return next;
	}

	public void setNext(Q2Node next) {
		this.next = next;
	}
	
}
